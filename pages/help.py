"""Help page describing the expected file format."""

from __future__ import annotations

import streamlit as st

st.set_page_config(
    page_title="Помощь — Анализатор Трафика",
    page_icon="❓",
)

st.title("❓ Помощь: Формат входных данных")

st.markdown(
    """
Это приложение принимает CSV-файлы со данными о посещаемости веб-сайта.
"""
)

st.subheader("Обязательные столбцы")

st.dataframe(
    {
        "Столбец": ["date", "sessions"],
        "Тип данных": ["Дата (YYYY-MM-DD)", "Число (int / float)"],
        "Описание": [
            "Дата посещения в формате ISO 8601",
            "Количество сессий за данный день",
        ],
    },
    hide_index=True,
    use_container_width=True,
)

st.subheader("Пример CSV-файла")

st.code(
    """date,sessions
2022-01-01,1037
2022-01-02,1098
2022-01-03,1690
2022-01-04,1517
2022-01-05,1451""",
    language="csv",
)

st.subheader("Правила")

st.info(
    """
- Столбцы **date** и **sessions** обязательны. Другие столбцы игнорируются.
- Разделитель — запятая (`,`).
- Даты должны быть в формате **YYYY-MM-DD** (например, `2022-01-01`).
- Значения в столбце **sessions** могут содержать `NaN` (пропуски данных).
- Минимальное количество строк для анализа — **1**.
- Рекомендуется: данные за период от нескольких месяцев до нескольких лет.
"""
)

st.subheader("Как загрузить файл")

st.markdown(
    """
1. Перейдите на главную страницу **📊 Дашборд**.
2. В боковой панели нажмите **«Загрузить CSV»** и выберите файл на вашем компьютере.
3. Если файл не загружен, приложение автоматически использует файл
   **`synthetic_traffic.csv`**, находящийся в директории приложения.
"""
)
